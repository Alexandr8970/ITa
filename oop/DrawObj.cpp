#include "DrawObj.h"


//-----------------------------------------------------------------------------
DrawObj::DrawObj(eTypeObj aType) 
    : mX(400)
     ,mY(300)
     ,mTypeObj(aType)
//-----------------------------------------------------------------------------
{
}

//-----------------------------------------------------------------------------
DrawObj::~DrawObj()
//-----------------------------------------------------------------------------
{
}

//-----------------------------------------------------------------------------
void DrawObj::setPosition(int aX, int aY)
{
    mX = aX;
    mY = aY;
}

//-----------------------------------------------------------------------------
const DrawObj::eTypeObj &DrawObj::getTypeObj()const
//-----------------------------------------------------------------------------
{
    return mTypeObj;
}

//-----------------------------------------------------------------------------
void DrawObj::incX()
//-----------------------------------------------------------------------------
{
    mX += 4;
}

//-----------------------------------------------------------------------------
void DrawObj::incY()
//-----------------------------------------------------------------------------
{
    mY += 4;
}

//-----------------------------------------------------------------------------
void DrawObj::decX()
//-----------------------------------------------------------------------------
{
    mX -= 4;
}

//-----------------------------------------------------------------------------
void DrawObj::decY()
//-----------------------------------------------------------------------------
{
    mY -= 4;
}

//-----------------------------------------------------------------------------
const int &DrawObj::getX() const
//-----------------------------------------------------------------------------
{
    return mX;
}

//-----------------------------------------------------------------------------
const int &DrawObj::getY() const
//-----------------------------------------------------------------------------
{
    return mY;
}

//-----------------------------------------------------------------------------
void DrawObj::draw()
//-----------------------------------------------------------------------------
{
    glClear(GL_COLOR_BUFFER_BIT);
    glMatrixMode(GL_MODELVIEW);
    glLoadIdentity();
}


